-- Remaps

-- External clipboard
vim.keymap.set({ 'n', 'v' }, '<leader>y', '"+y', {})
vim.keymap.set({ 'n', 'v' }, '<leader>p', '"+p', {})

-- Access explorer
vim.keymap.set('n', '<leader>pv', ':Ex<CR>')

-- hlsearch reset
vim.keymap.set('n', '<leader>nh', ':nohlsearch<CR>')

vim.keymap.set('v', 'J', ":m '>+1<CR>gv=gv")
vim.keymap.set('v', 'K', ":m '>-2<CR>gv=gv")

